export function compressColor(str: string) {
  if (str.startsWith("#")) {
    str = str.toLowerCase();
  }
  if (str.toLowerCase() in NAMED_COLOR_MAP) {
    return NAMED_COLOR_MAP[str.toLowerCase()];
  }
  return str;
}
